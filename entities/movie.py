class Movies:

    def __init__(self, movie_id, movie_title, release_date, rating, boxoffice_id, studio_id, director_id):
        self.movie_id = movie_id
        self.movie_title = movie_title
        self.release_date = release_date
        self.rating = rating
        self.boxoffice_id = boxoffice_id
        self.studio_id = studio_id
        self.director_id = director_id